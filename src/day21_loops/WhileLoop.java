package day21_loops;

public class WhileLoop {
    public static void main(String[] args) {

        int num = 1;
        while(num <= 100){

            System.out.println("Hello World " + num++);
        }

        int z=1;
        while (z<=50) {
            System.out.println("Print z "+z);
            ++z;
        }
    }
}
